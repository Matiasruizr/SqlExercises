---- TABLA PAIS ----
CREATE TABLE PAIS(
ID NUMBER NOT NULL,
NOMBRE VARCHAR2(25) NOT NULL,
CONSTRAINT PK_PAIS PRIMARY KEY(ID)
);

---- TABLA REGION ----
CREATE TABLE REGION(
ID NUMBER NOT NULL,
NOMBRE VARCHAR2(65) NOT NULL
);

ALTER TABLE REGION
ADD ID_PAIS NUMBER NOT NULL;

ALTER TABLE REGION
ADD CONSTRAINT PK_REGION PRIMARY KEY(ID);

ALTER TABLE REGION
ADD CONSTRAINT FK_REGION_PAIS FOREIGN KEY(ID_PAIS)
REFERENCES PAIS(ID);

---- TABLA COMUNA ----

CREATE TABLE COMUNA(
ID NUMBER PRIMARY KEY,
NOMBRE VARCHAR2(20) NOT NULL,
ID_REGION NUMBER NOT NULL,
CONSTRAINT FK_COMUNA_REGION FOREIGN KEY(ID_REGION)
REFERENCES REGION(ID)
);

---- TABLA PERSONA ----

CREATE TABLE PERSONA(
RUT NUMBER NOT NULL,
DV VARCHAR2(1) NOT NULL,
PRIMER_NOMBRE VARCHAR2(30) NOT NULL,
SEGUNDO_NOMBRE VARCHAR2(30),
APELLIDO_PATERNO VARCHAR2(30) NOT NULL,
APELLIDO_MATERNO VARCHAR2(30),
SEXO VARCHAR2(1) NOT NULL,
FECHA_NACIMIENTO DATE NOT NULL,
ESTADO_CIVIL VARCHAR2(12) NOT NULL,
PAIS_ORIGEN NUMBER NOT NULL,
DIRECCION VARCHAR2(150) NOT NULL,
COMUNA NUMBER NOT NULL,	
TELEFONO VARCHAR2(15),
CORREO_ELECTRONICO VARCHAR2(200),
CONSTRAINT PK_PERSONA PRIMARY KEY(RUT),
CONSTRAINT FK_PERSONA_PAIS FOREIGN KEY(PAIS_ORIGEN)
REFERENCES PAIS(ID),
CONSTRAINT FK_PERSONA_COMUNA FOREIGN KEY(COMUNA)
REFERENCES COMUNA(ID)
);

---- TABLA EMPRESA ----

CREATE TABLE EMPRESA(
RUT NUMBER NOT NULL,
DV VARCHAR2(1) NOT NULL,
RAZON_SOCIAL VARCHAR2(150) NOT NULL,
NOMBRE_FANTASIA VARCHAR2(150) NOT NULL,
GIRO VARCHAR2(250) NOT NULL,
DIRECCION VARCHAR2(150) NOT NULL,
COMUNA NUMBER NOT NULL,
REPRESENTANTE_LEGAL NUMBER NOT NULL,
TELEFONO VARCHAR2(15) NOT NULL,
CANTIDAD_EMPLEADOS NUMBER,
SITIO_WEB VARCHAR2(100),
CORREO_ELECTRONICO VARCHAR2(200),
CONSTRAINT PK_EMPRESA PRIMARY KEY(RUT),
CONSTRAINT FK_EMPRESA_COMUNA FOREIGN KEY(COMUNA)
REFERENCES COMUNA(ID),
CONSTRAINT FK_EMPRESA_REPRESENTANTE FOREIGN KEY(REPRESENTANTE_LEGAL)
REFERENCES PERSONA(RUT)
);

---- TABLA SUCURSAL ----

CREATE TABLE SUCURSAL(
ID NUMBER NOT NULL,
RUT_EMPRESA NUMBER NOT NULL,
DIRECCION VARCHAR2(150) NOT NULL,
COMUNA NUMBER NOT NULL,
TELEFONO VARCHAR2(15),
CORREO_ELECTRONICO VARCHAR2(200),
CONSTRAINT FK_SUCURSAL_EMPRESA FOREIGN KEY(RUT_EMPRESA)
REFERENCES EMPRESA(RUT),
CONSTRAINT FK_SUCURSAL_COMUNA FOREIGN KEY(COMUNA)
REFERENCES COMUNA(ID),
CONSTRAINT PK_SUCURSAL PRIMARY KEY (ID)
);

---- TABLA EMPLEADO ----

CREATE TABLE EMPLEADO(
ID NUMBER NOT NULL,
RUT_EMPRESA NUMBER NOT NULL,
PERSONA NUMBER NOT NULL,
CARGO VARCHAR2(50) NOT NULL,
SUELDO NUMBER NOT NULL,
CONSTRAINT FK_EMPLEADO_EMPRESA FOREIGN KEY(RUT_EMPRESA)
REFERENCES EMPRESA(RUT),
CONSTRAINT FK_PERSONA_EMPLEADO FOREIGN KEY(PERSONA)
REFERENCES PERSONA(RUT),
CONSTRAINT PK_EMPLEADO PRIMARY KEY (ID)
);

---- TABLA MARCA ----

CREATE TABLE MARCA(
ID NUMBER NOT NULL,
NOMBRE VARCHAR2(75) NOT NULL,
PAIS_ORIGEN NUMBER NOT NULL,
CONSTRAINT PK_MARCA PRIMARY KEY (ID),
CONSTRAINT FK_MARCA_PAIS FOREIGN KEY(PAIS_ORIGEN)
REFERENCES PAIS(ID)
);

---- TABLA CLIENTE ----

CREATE TABLE CLIENTE(
ID NUMBER NOT NULL,
FECHA_INGRESO DATE NOT NULL,
RUT_CLIENTE NUMBER NOT NULL,
CONSTRAINT PK_CLIENTE PRIMARY KEY (ID),
CONSTRAINT FK_CLIENTE_PERSONA FOREIGN KEY (RUT_CLIENTE)
REFERENCES PERSONA(RUT)
);

---- TABLA PRODUCTO ----

CREATE TABLE PRODUCTO(
ID NUMBER NOT NULL,
NOMBRE VARCHAR2(75) NOT NULL,
LOTE VARCHAR(20),
FECHA_VENCIMIENTO DATE,
MARCA NUMBER NOT NULL,
VALOR_NETO NUMBER NOT NULL,
CONSTRAINT PK_PRODUCTO PRIMARY KEY(ID),
CONSTRAINT FK_PRODUCTO_MARCA FOREIGN KEY(MARCA)
REFERENCES MARCA(ID)
);
---- TABLA FORMA DE PAGO ----

CREATE TABLE FORMA_PAGO(
ID NUMBER NOT NULL,
NOMBRE_PAGO VARCHAR2(75) NOT NULL,
CONSTRAINT PK_FORMA_PAGO PRIMARY KEY(ID)
);
---- TABLA FACTURA ----

CREATE TABLE FACTURA(
FOLIO NUMBER NOT NULL,
EMPRESA_VENDEDORA NUMBER NOT NULL,
EMPLEADO_VENTA NUMBER NOT NULL,
EMPRESA_CLIENTE NUMBER NOT NULL,
VALOR_NETO NUMBER NOT NULL,
FECHA_VENTA DATE NOT NULL,
IVA NUMBER NOT NULL,
DESCUENTO NUMBER,
TOTAL NUMBER NOT NULL,
FORMA_PAGO NUMBER NOT NULL,
CONSTRAINT PK_FACTURA PRIMARY KEY(FOLIO, EMPRESA_VENDEDORA),
CONSTRAINT FK_FACTURA_EMPRESA FOREIGN KEY(EMPRESA_VENDEDORA)
REFERENCES EMPRESA(RUT),
CONSTRAINT FK_FACTURA_CLIENTE FOREIGN KEY(EMPRESA_CLIENTE)
REFERENCES CLIENTE(ID),
CONSTRAINT FK_FACTURA_VENDEDOR FOREIGN KEY(EMPLEADO_VENTA)
REFERENCES EMPLEADO(ID),
CONSTRAINT FK_FACTURA_PAGO FOREIGN KEY(FORMA_PAGO) 
REFERENCES FORMA_PAGO(ID)
);

---- TABLA DETALLE FACTURA ----

CREATE TABLE DETALLE_FACTURA(
FOLIO_FACTURA NUMBER NOT NULL,
PRODUCTO NUMBER NOT NULL,
CANTIDAD NUMBER NOT NULL,
TOTAL NUMBER NOT NULL,
CONSTRAINT FK_DETALLE_FACTURA FOREIGN KEY (FOLIO_FACTURA) 
REFERENCES FACTURA(FOLIO),
CONSTRAINT FK_DETALLE_PRODUCTO FOREIGN KEY (PRODUCTO) 
REFERENCES PRODUCTO(ID),
CONSTRAINT PK_DETALLE_FACTURA PRIMARY KEY(FOLIO_FACTURA,PRODUCTO)
);


